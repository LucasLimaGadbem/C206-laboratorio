package org.example;

public class Arma {

    private String nomeArma;
    private boolean magica;

    //getter e setter para variaves da classe arma

    //getter de nomeArma
    public String getNomeArma() {
        return nomeArma;
    }

    //setter de nomeArma
    public void setNomeArma(String nomeArma) {
        this.nomeArma = nomeArma;
    }

    //getter de magica
    public boolean getMagica() {
        return magica;
    }

    //setter de magica
    public void setMagica(boolean magica) {
        this.magica = magica;
    }
}
